class PromptGenLSKBlock(nn.Module):
    def __init__(self, in_dim, prompt_dim=128, prompt_len=5, prompt_size = 96):
        super(PromptGenLSKBlock, self).__init__()
        
        self.conv0 = nn.Conv2d(in_dim, in_dim, 5, padding=2, groups=in_dim)
        self.conv_spatial = nn.Conv2d(in_dim, in_dim, 7, stride=1, padding=9, groups=in_dim, dilation=3)
        self.conv1 = nn.Conv2d(in_dim, in_dim // 2, 1)
        self.conv2 = nn.Conv2d(in_dim, in_dim // 2, 1)
        self.conv_squeeze = nn.Conv2d(2, prompt_len, kernel_size=3, stride=1, padding=1)
        self.conv3x3 = nn.Conv2d(prompt_dim,prompt_dim,kernel_size=3,stride=1,padding=1,bias=False)
        self.linear = nn.Linear(prompt_len, prompt_len)
        
        self.prompt_param = nn.Parameter(torch.rand(1,prompt_len,prompt_dim,prompt_size,prompt_size))

    def forward(self, x):
        B,C,H,W = x.shape
        attn1 = self.conv0(x)
        attn2 = self.conv_spatial(attn1)

        attn1 = self.conv1(attn1)
        attn2 = self.conv2(attn2)

        attn = torch.cat([attn1, attn2], dim=1)
        avg_attn = torch.mean(attn, dim=1, keepdim=True)
        max_attn, _ = torch.max(attn, dim=1, keepdim=True)
        agg = torch.cat([avg_attn, max_attn], dim=1)
        prompt_weights = self.conv_squeeze(agg).sigmoid().mean(dim=(-2,-1))
        prompt_weights = self.linear(prompt_weights)
        prompt = prompt_weights.unsqueeze(-1).unsqueeze(-1).unsqueeze(-1) * self.prompt_param.unsqueeze(0).repeat(B,1,1,1,1,1).squeeze(1)
        prompt = torch.sum(prompt,dim=1)
        prompt = F.interpolate(prompt,(H,W),mode="bilinear")
        prompt = self.conv3x3(prompt)
        
        return prompt


